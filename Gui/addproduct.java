    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String pname = jTextField1.getText();

        if (pname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Product Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs = MySQL.search("SELECT * FROM `product` WHERE `pname`='" + pname + "' AND `status_id`='1' ");

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This Product Already Added!", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `product`(`pname`,`status_id`,`company`) VALUES('" + pname + "','1','1') ; ");

                    loadproduct();
                    jTextField1.setText("");
                    h.loadproduct();
                    JOptionPane.showMessageDialog(this, "Product Added Succesfull!", "Suucess", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
          if (evt.getClickCount() == 2) {

            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a product", "warning", JOptionPane.WARNING_MESSAGE);
            } else {
            
                String id = jTable1.getValueAt(r, 0).toString();
                
                int selected = JOptionPane.showConfirmDialog(this, "Do You Want Delete Product","Choose",JOptionPane.YES_NO_OPTION);
                
                if(selected == JOptionPane.YES_OPTION){
                
                       MySQL.iud("UPDATE `product` SET `status_id`='2' WHERE `id`='"+id+"' ");
                       loadproduct();
                       h.loadproduct();
                    
                }                
            }
 
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

