    public void loadproduct() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `product` INNER JOIN `company` ON `company`.`id`=`product`.`company` WHERE `company`.`name` LIKE '%DSL%' AND `status_id`='1' ORDER BY `product`.`id` ASC ; ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("pname"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

