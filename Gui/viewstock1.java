    public void loadstock() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `stock` INNER JOIN `company` ON `company`.`id`=`stock`.`company_id` INNER JOIN `product` ON `product`.`id`=`stock`.`product_id` INNER JOIN `payment_type` ON `payment_type`.`id`=`stock`.`payment_type_id` WHERE `stock`.`status_id`='1' ORDER BY `stock`.`id` ASC ; ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("stock.id"));
                v.add(rs.getString("date"));
                v.add(rs.getString("company.name"));
                v.add(rs.getString("product.pname"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("unit_price"));
                v.add(rs.getString("total"));
                v.add(rs.getString("payment_type.name"));
                v.add(rs.getString("payment"));
                v.add(rs.getString("balance"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

