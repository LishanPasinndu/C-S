    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {

            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a Stock", "warning", JOptionPane.WARNING_MESSAGE);
            } else {

                String sid = jTable1.getValueAt(r, 0).toString();
                String ddate = jTable1.getValueAt(r, 1).toString();
                String company = jTable1.getValueAt(r, 2).toString();
                String product = jTable1.getValueAt(r, 3).toString();
                String uprice = jTable1.getValueAt(r, 5).toString();
                String qty = jTable1.getValueAt(r, 4).toString();
                String total = jTable1.getValueAt(r, 6).toString();

                h.gid.setText(sid);
                h.gcname.setText(company);
                h.gpname.setText(product);
                h.gdate.setText(ddate);
                h.gqty.setText(qty);
                h.guprice.setText(uprice);
                h.gtotal.setText(total);
                h.groute.setText(sid);
                h.gref.setText("None");

                this.dispose();

            }
        }


    }//GEN-LAST:event_jTable1MouseClicked

